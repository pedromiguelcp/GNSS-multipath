function plot_polar(handle)
hlinex=[-0.9 0.9]; hliney=[0 0]; vlinex=[0 0]; vliney=[-0.9 0.9];
arg=[0:100]*2*pi/100;
plot(handle,0.9*sin(arg),0.9*cos(arg),'-',...
    0.8*sin(arg),0.8*cos(arg),'-',...
    0.7*sin(arg),0.7*cos(arg),'-',...
    0.6*sin(arg),0.6*cos(arg),'-',...
    0.5*sin(arg),0.5*cos(arg),'-',...
    0.4*sin(arg),0.4*cos(arg),'-',...
    0.3*sin(arg),0.3*cos(arg),'-',...
    0.2*sin(arg),0.2*cos(arg),'-',...
    0.1*sin(arg),0.1*cos(arg),'-',...
    hlinex,hliney,'-',vlinex,vliney,'-');
axis(handle,'square');
hold(handle, 'on');
q1 = [0.9*cos(pi/4) 0.9*sin(pi/4)];
q4 = [0.9*cos(-pi/4) 0.9*sin(-pi/4)];
q2 = -q4;
q3 = -q1;
plot(handle,[0;q1(1)],[0;q1(2)],'--k',...
    [0;q2(1)],[0;q2(2)],'--k',...
    [0;q3(1)],[0;q3(2)],'--k',...
    [0;q4(1)],[0;q4(2)],'--k');
text('Parent',handle,'Position',[0.0 0.95],'String','N');
text('Parent',handle,'Position',[0.95 0.0],'String','E');
text('Parent',handle,'Position',[-0.95 0.0],'String','W');
text('Parent',handle,'Position',[0.0 -0.95],'String','S');
set(handle,'YTickLabel',[]);
set(handle,'XTickLabel',[]);
set(handle,'xtick',[]);
set(handle,'ytick',[]);
end